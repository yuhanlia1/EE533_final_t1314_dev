# ---------------------------------------------------------------------------
# Convert the “middle 16” hex words of a list into nw_in_* stimulus blocks
# ---------------------------------------------------------------------------

# hex_words = ['000000000000', '00000000000000DB', '00000000000000E0', '0000000000000000', '00000000000000CB', '00000000000000EF', '0000000000000000', '00000000000000FF', '0000000000000000', '0000000000000000', '0000000000000000', '00000000000000E0', '00000000000000CA', '00000000000000C6', '00000000000000A6', '00000000000000EB', '00000000000000EE']

hex_words = ['000000000000', '00000000000000EA', '0000000000000077', '0000000000000000', '00000000000000FE', '00000000000000DA', '0000000000000000', '0000000000000000', '0000000000000000', '0000000000000000', '0000000000000000', '0000000000000077', '00000000000000F4', '000000000000008A', '00000000000000B6', '00000000000000D5', '00000000000000D5']

# --- keep only the “middle 16” elements ------------------------------------
middle_16 = hex_words[1:]

if len(middle_16) != 16:
    raise ValueError("Expected exactly 16 middle elements, got "
                     f"{len(middle_16)} instead")

# --- emit nw_in_* lines -----------------------------------------------------
for word in middle_16:
    padded_hex = word.upper().zfill(16)    # 16 hex digits, upper-case
    print("  nw_in_wr   = 1;")
    print(f"  nw_in_data = 64'h{padded_hex};")
    print("  nw_in_ctrl = 8'h00;")
    print("  #10;")
